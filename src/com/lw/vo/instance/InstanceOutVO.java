package com.lw.vo.instance;

public class InstanceOutVO
{
	/** 副本名称 */
	private String instanceName;
	/** 副本等级 */
	private String instanceLv;
	/** 副本时间 */
	private String instanceTime;

	public String getInstanceName()
	{
		return instanceName;
	}

	public void setInstanceName(String instanceName)
	{
		this.instanceName = instanceName;
	}

	public String getInstanceLv()
	{
		return instanceLv;
	}

	public void setInstanceLv(String instanceLv)
	{
		this.instanceLv = instanceLv;
	}

	public String getInstanceTime()
	{
		return instanceTime;
	}

	public void setInstanceTime(String instanceTime)
	{
		this.instanceTime = instanceTime;
	}
}
